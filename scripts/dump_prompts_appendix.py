#!/usr/bin/env python3
"""Concatenate registered prompt templates into appendix/PROMPTS_APPENDIX.md."""

from __future__ import annotations

import hashlib
import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
PROMPTS = ROOT / "configs" / "prompts"
OUT = ROOT / "appendix" / "PROMPTS_APPENDIX.md"
FILES = [
    "text_only_v1.json",
    "code_only_v1.json",
    "naive_concat_v1.json",
    "full_method_v1.json",
]


def main() -> int:
    OUT.parent.mkdir(parents=True, exist_ok=True)
    parts: list[str] = [
        "# Appendix: prompt templates (frozen dump)\n",
        "\nThis file is generated by `scripts/dump_prompts_appendix.py`.\n",
        "\n",
    ]
    for name in FILES:
        p = PROMPTS / name
        raw = p.read_text(encoding="utf-8")
        h = hashlib.sha256(raw.encode("utf-8")).hexdigest()
        parts.append(f"## `{name}`\n\n")
        parts.append(f"- sha256: `{h}`\n\n")
        parts.append("```json\n")
        parts.append(json.dumps(json.loads(raw), indent=2))
        parts.append("\n```\n\n")
    OUT.write_text("".join(parts), encoding="utf-8")
    print(f"wrote {OUT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
